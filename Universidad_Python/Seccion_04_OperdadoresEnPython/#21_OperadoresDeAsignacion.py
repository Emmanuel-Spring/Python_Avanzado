"""Operadores de asignación y resignación"""

miVariable = 10
print(miVariable)
print("***************************")
miVariable = miVariable + 1
print(miVariable)
print("****************************")
miVariable += 5
print(miVariable)
print("*****************************")
miVariable -= 9
print(miVariable)
print("*****************************")
miVariable *= 30
print(miVariable)
print("*****************************")
miVariable /= 5
print(miVariable)