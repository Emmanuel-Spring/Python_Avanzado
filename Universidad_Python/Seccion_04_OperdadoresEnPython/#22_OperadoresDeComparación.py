""" Vídeo N° 22 Operadores de comparación en Python
Operadores : ==  !=  <  >  <=  >=
"""

a = 4
b = 2
print("Números con a = 4 y b = 2")

resultado = a == b
print(f"Resultado de == (a == b): {resultado}")
print(resultado)

print("**************************************")
resultado = a != b
print(f"Resultado de != (a != b): {resultado}")

print("**************************************")
resultado = a > b
print(f"Resultado de > (a > b): {resultado}")

print("**************************************")
resultado = a >= b
print(f"Resultado de >= (a >= b): {resultado}")

print("**************************************")
resultado = a < b
print(f"Resultado de < (a < b): {resultado}")

print("**************************************")
resultado = a <= b
print(f"Resultado de <= (a <= b): {resultado}")

